local mysc = require("util.mysc")
-- =========================================================>
--  [Table] This:
-- =========================================================>
local this = {}
-- =========================================================>
--  [Functions] Link:
-- =========================================================>
function this.to(c, w, name)
    if (type(w) == "table") then
        w = mysc.to_widget(w)
    end
    c[name] = w
    return w
end
-- =========================================================>
function this.tooltip(t, w)
    w = mysc.to_widget(w)
    t:add_to_object(w)
    return w
end
-- =========================================================>
return this
