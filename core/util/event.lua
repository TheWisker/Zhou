local mysc = require("util.mysc")
-- =========================================================>
--  [Table] This:
-- =========================================================>
local this = {}
-- =========================================================>
--  [Functions] Event:
-- =========================================================>
--> Connects function (f) to widget (w)'s signal (s):
-- =========================================================>
function this.connect(w, f, s, off)
    w = mysc.to_widget(w)
    if (not off) then
        w:connect_signal(s, f)
    end
    return w
end
-- =========================================================>
return this
